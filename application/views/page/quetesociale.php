		<li>Porte de Namur, </li>
		<li>Delta, </li>
		<li>Campus d'Erasme, </li>
		<li>Cimetière d'Ixelles, </li>
		<li>... </li>
	  </ul>
	  Dès 8h et jusqu'à 20 heures, ils y seront visibles grâce à des banderoles CAP 48 et des badges d'accréditation. Cette année encore, réservez-leur un bon accueil et faites en don en faveur de CAP 48 ! Merci ! </p>
	
